#include "ft_printf.h"

char		*ft_toupper(char * str)
{
	int 	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 97 && str[i] <= 122)
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
